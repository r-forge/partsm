<h2>partsm R package</h2>

<p>This package performs basic functions to fit and predict periodic autoregressive time series models as discussed in the book <a href="http://books.google.es/books?id=IJmMhRV93WsC">"Periodicity and Stochastic Trends in Economic Time Series"</a> by Philip Hans Franses. Data sets analyzed in that book are also provided.</p>

<p>You can find it in <a href="http://cran.r-project.org/web/packages/partsm/index.html">CRAN</a> and can be installed using R's install.package function.</p>

<p>The project summary page can be found <a href="http://r-forge.r-project.org/projects/partsm/">here</a>. You may also be interested in its page at <a href="http://crantastic.org/packages/partsm">crantastic</a>.</p>

<p>The package was originally developed by Javier López-de-Lacalle and it is currently mantained by <a href="http://www.datanalytics.com">Carlos J. Gil Bellosta</a>.</p>
